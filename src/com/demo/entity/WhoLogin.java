package com.demo.entity;

public class WhoLogin {
	String login_name;
	String login_time;
	String login_ip;
	String login_system;
	String login_browser;
	public String getLogin_name() {
		return login_name;
	}
	public void setLogin_name(String login_name) {
		this.login_name = login_name;
	}
	public String getLogin_time() {
		return login_time;
	}
	public void setLogin_time(String login_time) {
		this.login_time = login_time;
	}
	public String getLogin_ip() {
		return login_ip;
	}
	public void setLogin_ip(String login_ip) {
		this.login_ip = login_ip;
	}
	public String getLogin_system() {
		return login_system;
	}
	public void setLogin_system(String login_system) {
		this.login_system = login_system;
	}
	public String getLogin_browser() {
		return login_browser;
	}
	public void setLogin_browser(String login_browser) {
		this.login_browser = login_browser;
	}
	
}
